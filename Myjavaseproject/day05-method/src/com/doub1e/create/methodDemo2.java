package com.doub1e.create;

public class methodDemo2 {
    public static void main(String[] args) {
        //理解方法的其他定义形式
        printHelloWorld();
    }
    //方法如果不需要接收数据 形参列表可以不写
    //方法如果不需要返回数据 返回值类型必须声明成void
    //无参数 无返回值类型
    public static void printHelloWorld(){
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }
}
